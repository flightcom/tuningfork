		<h3>La base de l’instrumenthèque : le don et la récupération</h3>
		<p>Aujourd’hui, nous mettons de côté des objets que l’on juge « dépassés », « vieux » et « abimés ». Les instruments de musique n’échappent pas à cette règle. Tout comme ces objets du quotidien, ils sont encore en état de fonctionnement et prêt à un nouvel emploi après quelques rafraîchissements. Notre association sensible aux questions d’environnement, propose de les récupérer et de les revaloriser en les mettant à la disposition de tous dans l’instrumenthèque.</p>
