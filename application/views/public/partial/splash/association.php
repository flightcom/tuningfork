		<h3>L'association derrière l'Instrumenthèque</h3>
		<p>L'association de loi 1901 Tuning Fork a été fondée en juin 2013 à Nantes. Elle a pour objectif de rendre la pratique de la musique accessible à tous dans un esprit solidaire et par le réemploi d'instruments de musique. Celle-ci développe des actions simultanées sur le libre accès à un instrument, son entretien et son apprentissage.</p>
